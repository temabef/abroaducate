It’s also worth noting that many programs in countries like Australia and some European nations, such as Finland, often require a research proposal. So, if you’re targeting these regions, keep in mind that you may need to write a research proposal as part of your application. Preparing this document thoughtfully can demonstrate your commitment to research and give the admissions committee a clear sense of your academic potential.
## **Documents Specific to Undergraduate Admissions**
Undergraduate programs focus more on your high school achievements and potential for academic growth. The following documents are typically required only for undergraduate admissions.
### **1. High School Diploma or Equivalent**
Proof of high school completion is mandatory. If your country has equivalent certifications, such as A-levels, WAEC, or the IB Diploma, these will usually be accepted as well.
### **2. Marksheets and Pass Certificates**
Marksheets and Pass Certificates are crucial documents that detail your academic performance throughout high school, often listing individual subject grades and confirming that you’ve met graduation requirements. I recommend that you collect all grade reports, marksheets, academic transcripts, and final examination results from your high school.

To gather these, visit your secondary school and speak to the school principal or administrative office—they’ll typically be able to provide everything you need, from Junior Secondary School (JSS) through Senior Secondary School (SSS). Also, it’s helpful to ensure that your documents include an explanation of your high school grading system, as this helps international admissions teams interpret your grades accurately. Many schools provide this on the back or last page of the transcript, but if not, ask if the school can add it. Having a complete and well-organized set of marksheets and transcripts will streamline the process and ensure your application stands strong.
### **3. Entrance Exam Scores (SAT, ACT)**
Many undergraduate programs, especially in the U.S., require entrance exam scores such as the SAT or ACT as part of their admissions criteria. I recommend taking the SAT or ACT if you’re aiming for undergraduate admission in the United States, as these exams play a significant role in the application process and can enhance your profile.

I understand that for students from Nigeria and other African countries, the SAT or ACT may not be a familiar requirement, as most universities here don’t ask for these scores. If this is the first time you’re hearing about these exams, that’s completely normal! The SAT (Scholastic Assessment Test) and the ACT (American College Testing) are standardized tests that assess skills in math, reading, writing, and other subjects and are commonly used by U.S. universities to evaluate applicants.

To get a better understanding of what the SAT or ACT entails, I recommend looking up resources online. You’ll find information on what each exam covers, how to register, and where you can take it, whether that’s in your home country or a nearby test center. Preparing for and taking these exams could open doors for you in the U.S., so it’s worth reading up and learning how they can support your application journey.
### **4. Additional Essays (If Required by the Program)**
Some programs may ask for additional essays to gain a deeper insight into your background, values, and ambitions. This is particularly common in competitive programs, where admissions teams are looking for what sets you apart from other applicants. If you encounter these essay requirements, it’s important to focus on crafting a strong narrative essay that genuinely reflects who you are.

I recommend putting time and thought into these essays, as they’re a great opportunity to showcase your unique qualities. For guidance, feel free to follow me on Twitter, where I share essay samples that helped students gain admission to top U.S. universities, including Harvard, Yale, Princeton, and more. These examples can be a helpful resource as you develop your own story and structure, giving you an idea of what a successful application essay can look like.
## **Understanding University and Country-Specific Requirements**
It’s essential to research the requirements for each university and country you’re applying to, as they can vary widely. Some universities might not require certain documents, while others may have additional requirements like interviews or medical certificates. Always double-check each program’s admissions page to ensure you’re meeting their criteria precisely.
## **How to Organize and Submit Your Documents**
