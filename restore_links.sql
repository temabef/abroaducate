### **6. Letters of Recommendation**
Another critical component of your application is the Letter of Recommendation. Typically, universities request 2-3 letters from people like professors, employers, or mentors who know you well academically or professionally. These letters should highlight qualities that make you an ideal candidate and help admissions teams see your strengths through someone else’s perspective.

For me, building connections with my professors before I graduated was key. I knew I’d eventually need recommendations, so I made an effort to engage with them during my studies. Many people struggle with finding recommenders because they haven’t built that connection over time, which is so important. I’d recommend starting to connect with professors, supervisors, or mentors as early as you can, so when the time comes, you’ll feel confident asking them to support your application.
### **7. Proof of Funds**
Proof of Funds is another essential document, especially for the visa application process. If you’ve received a full-ride scholarship or your graduate program has offered you a full tuition waiver with a stipend, then you can use that as your proof of funds. However, if you’re self-funding, you’ll need to show evidence of financial resources—either yours or your sponsor’s.

In my experience, self-funding applicants should be aware that many universities, especially those in the UK, usually require a deposit to secure your place in the program. So, having a clear financial plan and necessary documentation like bank statements or sponsorship letters is crucial to ensure you meet this requirement without delays.
### **8. Application Fee**
Though not a document, the application fee is something you’ll encounter in most applications. I recommend taking advantage of universities that offer fee waivers, as well as applying to those that don’t charge fees at all. Some universities also waive application fees during specific months—for instance, Alabama universities and colleges typically offer fee waivers in October, and other U.S. states may do this as well. Attending virtual events hosted by departments is another way to receive a fee waiver, as these events often provide waivers to attendees.

Another effective way to manage application fees is to directly request a waiver. Many universities provide fee waivers for applicants who demonstrate financial need or other qualifying circumstances.
