### **3. English Language Proficiency Test Scores**
As a non-native English speaker, proving language proficiency is often essential, and for many of us, that means taking the IELTS or TOEFL. However, if you’re a native English speaker or come from an Anglophone African country like Nigeria or Ghana, you might be in luck! Some universities, especially in the U.S. and Canada, are now exempting applicants from English-speaking African nations from this requirement.

My advice? Check the English language requirement for each university before you jump into booking an exam. Many schools have lists of exempt countries, so if you’re from one of these, you may not need to take the IELTS or TOEFL. Even if your country isn’t listed, but you’re a native English speaker, consider reaching out to your program coordinator. Politely ask if they’d be willing to provide a waiver for the English proficiency test—this small step could save you the cost and effort of taking these exams.

I also recommend collecting an English proficiency letter from the university where you graduated. This letter, which states that English was the medium of instruction, can often be obtained from the administrative office. Some universities may accept this letter in place of IELTS or TOEFL, so it’s worth checking with each school.

However, if all avenues have been exhausted and a waiver isn’t an option, then go ahead and take the IELTS or TOEFL.
