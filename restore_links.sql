## **Documents Specific to Graduate Admissions**
Graduate programs are often more specialized and demand additional documentation to verify your qualifications. Here are documents that are usually required for graduate-level applications only.
### **1. Bachelor’s/Master’s Degree or Equivalent**
Degree Certificates are essential when applying to graduate programs, as they confirm your academic qualifications. For master’s programs, you’ll need to show proof of your bachelor’s degree, and for some doctoral programs, both bachelor’s and master’s degrees are required. However, in the U.S., many doctoral programs allow you to apply with just a bachelor’s degree.

My advice? Make sure to get your bachelor’s or master’s certificate as soon as you graduate—don’t delay it. Having your certificates readily available can streamline your application process and avoid unnecessary stress.
### **2. Academic Transcripts**
Academic Transcripts are essential documents that verify your academic performance and provide admissions committees with a detailed record of your coursework and grades. I strongly recommend starting the process of obtaining your transcripts early, as processing times can vary significantly between universities.

From my experience and what I’ve heard from others, some universities in Nigeria can take over three months to issue transcripts. This long wait can lead to delays in your application process, so it’s best to plan ahead. By requesting your transcripts well in advance, you’ll ensure you have them ready when you need them, keeping your application on track without unnecessary delays.
### **3. Standardized Test Scores (GRE, GMAT, LSAT, MCAT)**
Standardized Test Scores (GRE, GMAT, LSAT, MCAT) are often required for admission to many graduate programs, depending on your field of study. Exams like the GRE or GMAT are valuable for applicants, especially those looking to strengthen their application or scholarship chances.

From my own experience, achieving a high score on these exams can significantly boost your admission prospects and may even increase your eligibility for scholarships. For instance, a high GRE score—say, above 330/340—can highlight academic improvement, which is especially helpful if you have a lower CGPA.

That said, many programs are beginning to waive these exams, so if you think taking them would be a financial burden, check the specific requirements for your program. However, I’d strongly recommend that anyone with a lower CGPA consider taking the GRE or a relevant test to enhance their application. Preparing well and scoring high on these tests can serve as a powerful tool to strengthen your overall profile.

